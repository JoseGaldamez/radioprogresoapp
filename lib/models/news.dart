class NewsModel {
  String title;
  String image;
  String date;
  String content;
  String url;
  String descripcion;
  NewsModel(title, image, date, content, url, descripcion) {
    this.title = title;
    this.image = image;
    this.date = date;
    this.content = content;
    this.url = url;
    this.descripcion = descripcion;
  }
}
